const mongoose = require('mongoose')
const mongodb = require('mongodb');
const Schema = mongodb.MongoClient.Schema

// Create Schema
const UserSchema = new Schema({
  first_name: {
    type: String
  },
  last_name: {
    type: String
  },
  email: {
    type: String,
    required: true
  },
  password: {
    type: String,
    required: true
  },
  date: {
    type: Date,
    default: Date.now
  }
})

module.exports = User = mongodb.MongoClient.model('users', UserSchema)